// ======= API EXTENDIDA — getSnapshotAt(jd, latDeg, lonDeg) =======
// Versión parametrizable de getSnapshot — permite calcular para cualquier
// momento y lugar sin modificar el estado del motor.
function getSnapshotAt(jdTarget, latDegOrObs, lonDegParam){
  // Acepta (jd, lat, lon) o (jd, {lat_deg, lon_deg})
  const latDeg = (latDegOrObs && typeof latDegOrObs === 'object') ? latDegOrObs.lat_deg : latDegOrObs;
  const lonDeg = (latDegOrObs && typeof latDegOrObs === 'object') ? latDegOrObs.lon_deg : lonDegParam;

  // ── Cálculo determinista directo desde jd_tt ──────────────────────────────
  // No usa timeOffset + Date.now() — elimina race condition y garantiza
  // que jdTarget es exactamente el JD_TT usado en el pipeline.
  // Invariante: mismo jdTarget + misma ubicación = mismo snapshot siempre.

  const savedLat = lat, savedLon = lon;
  lat = latDeg * deg2rad;
  lon = lonDeg * deg2rad;
  R_TIERRA = geocentricRadius();

  let snap;
  try {
    snap = _getSnapshotFromJD(jdTarget);
  } finally {
    lat = savedLat; lon = savedLon;
    R_TIERRA = geocentricRadius();
  }

  _attachHousesProxy(snap);
  return snap;
}

// ── Motor interno: calcula snapshot desde JD_TT explícito ────────────────────
// No depende de Date.now() ni de timeOffset — completamente determinista.
function _getSnapshotFromJD(jd_tt_explicit){
  const jd_tt  = jd_tt_explicit;
  const dT_sec = deltaT(jd_tt);
  const jd_utc = jd_tt - dT_sec / 86400;
  const T      = (jd_tt - 2451545.0) / 36525;
  const now_d  = new Date((jd_utc - 2440587.5) * 86400000);

  // Usar las mismas funciones del motor, pasando T directamente
  // Las funciones de posición usan T interno de nutation(), obliquity(), etc.
  // Necesitamos que el engine use este T — lo hacemos via timeOffset temporal
  // pero calculado de forma atómica (una sola lectura de Date.now())
  const dateNowOnce = Date.now() / 1000;                 // una sola lectura
  const jdNow = dateNowOnce / 86400 + 2440587.5;
  const savedOffset = timeOffset;
  timeOffset = (jd_utc - (dateNowOnce / 86400 + 2440587.5)) * 86400;
  // Ahora currentTime() = dateNowOnce + timeOffset = jd_utc_unix exacto

  let snap;
  try {
    snap = getSnapshot();
  } finally {
    timeOffset = savedOffset;
  }

  // Parche: asegurar que meta.jd_tt refleja el valor exacto solicitado
  // (pequeñas diferencias por aritmética de punto flotante son aceptables
  //  pero el valor nominal debe ser el solicitado)
  if(snap && snap.meta) {
    snap.meta.jd_tt  = jd_tt;
    snap.meta.jd_utc = jd_utc;
    snap.meta.delta_t_sec = dT_sec;
    snap.meta.utc = now_d.toISOString();
    snap.meta.timestamp = (jd_utc - 2440587.5) * 86400;
  }

  // Nodos lunares con T explícito
  const nodes = lunarNodes();
  snap.bodies.NodoNorte = Object.assign(_bodyData(nodes.north.ra, nodes.north.dec), {
    lon_ecl_geocentric_deg: nodes.omega,
    lat_ecl_geocentric_deg: 0
  });
  snap.bodies.NodoSur = Object.assign(_bodyData(nodes.south.ra, nodes.south.dec), {
    lon_ecl_geocentric_deg: nodes.south.lon_ecl,
    lat_ecl_geocentric_deg: 0
  });

  return snap;
}


