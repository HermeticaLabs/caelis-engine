// Cache de nutación IAU 2000B
let _nutCache = null, _nutCacheT = null;

function nutation(T){
  if(_nutCache !== null && Math.abs(T - _nutCacheT) < 1e-9) return _nutCache;

  // ── Argumentos fundamentales IAU 2000A (Capitaine et al. 2003) ──
  // Anomalía media de la Luna (l)
  const l  = (485868.249036 + T*(1717915923.2178 + T*(31.8792
              + T*(0.051635 - T*0.00024470)))) % 1296000 * deg2rad / 3600;
  // Anomalía media del Sol (l')
  const lp = (1287104.793048 + T*(129596581.0481 + T*(-0.5532
              + T*(0.000136 - T*0.00001149)))) % 1296000 * deg2rad / 3600;
  // Argumento de la latitud de la Luna (F)
  const F  = (335779.526232 + T*(1739527262.8478 + T*(-12.7512
              + T*(-0.001037 + T*0.00000417)))) % 1296000 * deg2rad / 3600;
  // Elongación media de la Luna (D)
  const D  = (1072260.703692 + T*(1602961601.2090 + T*(-6.3706
              + T*(0.006593 - T*0.00003169)))) % 1296000 * deg2rad / 3600;
  // Longitud media del nodo lunar (Ω)
  const Om = (450160.398036 + T*(-6962890.5431 + T*(7.4722
              + T*(0.007702 - T*0.00005939)))) % 1296000 * deg2rad / 3600;

  let dPsi = 0, dEps = 0;
  for(const r of _IAU2000B){
    const arg = r[0]*l + r[1]*lp + r[2]*F + r[3]*D + r[4]*Om;
    dPsi += (r[5] + r[6]*T) * Math.sin(arg);
    dEps += (r[7] + r[8]*T) * Math.cos(arg);
  }

  // Convertir de 0.1 μas → radianes
  // 0.1 μas × (1/10) × (1/1e6) × (π/648000) rad/arcsec
  const factor = 0.1e-6 * deg2rad / 3600;
  _nutCache  = { deltaPsi: dPsi * factor, deltaEps: dEps * factor };
  _nutCacheT = T;
  return _nutCache;
}

// Oblicuidad media — fórmula IAU 2006 (Capitaine et al. 2006)
function meanObliquity(T){
  // Arcsegundos → radianes
  const eps0 = (84381.406 + T*(-46.836769 + T*(-0.0001831
               + T*(0.00200340 + T*(-0.000000576 - T*0.0000000434)))));
  return eps0 * deg2rad / 3600;
}

function trueObliquity(T){ return meanObliquity(T) + nutation(T).deltaEps; }


function gast(jd){
  let T=getJulianCenturies(jd);
  let gmst=86400*(0.7790572732640+1.00273781191135448*(jd-2451545));
  gmst=((gmst%86400)+86400)%86400;
  let gmstDeg=gmst/86400*360;
  let gmstCorr=(0.014506+4612.156534*T+1.3915817*T*T-0.00000044*T*T*T
               -0.000029956*T*T*T*T-0.0000000368*T*T*T*T*T)/3600;
  gmstDeg+=gmstCorr;
  let{deltaPsi}=nutation(T);
  let eqEq=deltaPsi*Math.cos(trueObliquity(T))*rad2deg;
  return((gmstDeg+eqEq)%360+360)%360;
}
function getLST(){
  let JD=julianDate();
  return(((gast(JD)+lon*rad2deg)%360)+360)%360*deg2rad;
}

function geocentricRadius(){
  let a=6378.137,b=6356.7523142,c=Math.cos(lat),s=Math.sin(lat);
  return Math.sqrt((Math.pow(a*a*c,2)+Math.pow(b*b*s,2))/(Math.pow(a*c,2)+Math.pow(b*s,2)));
}
